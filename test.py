def normalize(vector):
    '''
    Normalizes a vector
    '''
    if (len(vector) != 0):
        normalized_x = vector[0] / len(vector)
        normalized_z = vector[1] / len(vector)
        return [normalized_x, normalized_z]
    else:
        return [0,0]

